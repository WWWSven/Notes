int main() {
	int a = 1;
	int b = 2;
	printf("a = %d, b = %d \n", a, b); // a = 1, b = 2
	// 地址做参数
	swap(&a, &b);
	printf("a = %d, b = %d \n", a, b); // a = 2, b = 1

	return 0;
}